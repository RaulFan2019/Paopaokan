public class LivePlayBackLocEntityV2 {

    public double latitude;// 纬度
    public double longitude;// 经度
    public long Duration;// 跑步时间
    public float length;// 跑步长度
    public long playTime;// 播放时间
    public int pace;// 速度 (秒/米)
    public String lapStartTime;


    public LivePlayBackLocEntityV2(double latitude, double longitude, long duration,
                                   float length, long playTime, int pace, String lapStartTime) {
        this.latitude = latitude;
        this.longitude = longitude;
        this.Duration = duration;
        this.length = length;
        this.playTime = playTime;
        this.pace = pace;
        this.lapStartTime = lapStartTime;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public long getDuration() {
        return Duration;
    }

    public void setDuration(long duration) {
        Duration = duration;
    }

    public float getLength() {
        return length;
    }

    public void setLength(float length) {
        this.length = length;
    }

    public long getPlayTime() {
        return playTime;
    }

    public void setPlayTime(long playTime) {
        this.playTime = playTime;
    }

    public int getPace() {
        return pace;
    }

    public void setPace(int pace) {
        this.pace = pace;
    }

    public String getLapStartTime() {
        return lapStartTime;
    }

    public void setLapStartTime(String lapStartTime) {
        this.lapStartTime = lapStartTime;
    }
}
