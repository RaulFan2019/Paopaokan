package com.app.pao.entity.model;

import com.app.pao.entity.network.GetWorkoutListResult;

/**
 * Created by Raul on 2015/11/29.
 * 用于动态展示的对象
 */
public class DynamicListItemEntity {

    private GetWorkoutListResult.WorkoutEntity workoutEntity;
    private String title;

    public DynamicListItemEntity() {

    }

    public DynamicListItemEntity(GetWorkoutListResult.WorkoutEntity workoutEntity, String title) {
        super();
        this.workoutEntity = workoutEntity;
        this.title = title;
    }

    public GetWorkoutListResult.WorkoutEntity getWorkoutEntity() {
        return workoutEntity;
    }

    public void setWorkoutEntity(GetWorkoutListResult.WorkoutEntity workoutEntity) {
        this.workoutEntity = workoutEntity;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
