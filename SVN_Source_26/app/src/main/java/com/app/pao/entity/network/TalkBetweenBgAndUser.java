public class TalkBetweenBgAndUser {

    private String time;
    private String talkContent;
    private int type;

    public TalkBetweenBgAndUser() {
    }

    public TalkBetweenBgAndUser(String time, String talkContent, int type) {
        this.time = time;
        this.talkContent = talkContent;
        this.type = type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTalkContent() {
        return talkContent;
    }

    public void setTalkContent(String talkContent) {
        this.talkContent = talkContent;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
