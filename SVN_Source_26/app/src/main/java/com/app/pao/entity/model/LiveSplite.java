package com.app.pao.entity.model;

/**
 * 直播的Splite
 *
 * @author Raul
 *
 */
public class LiveSplite {

	public float length;// 长度(米)
	public long duration;// 时间(秒)
	public int avgheartrate;// 平均心率
	public double minaltitude;// 最低海拔
	public double maxaltitude;// 最高海拔
	public double latitude;// 纬度
	public double longitude;// 经度
	private int id;

	public LiveSplite() {

	}

	public LiveSplite(float length, long duration, int avgheartrate, double minaltitude, double maxaltitude,
					  double latitude, double longitude, int id) {
		super();
		this.length = length;
		this.duration = duration;
		this.avgheartrate = avgheartrate;
		this.minaltitude = minaltitude;
		this.maxaltitude = maxaltitude;
		this.latitude = latitude;
		this.longitude = longitude;
		this.id = id;
	}

	public float getLength() {
		return length;
	}

	public void setLength(float length) {
		this.length = length;
	}

	public long getDuration() {
		return duration;
	}

	public void setDuration(long duration) {
		this.duration = duration;
	}

	public int getAvgheartrate() {
		return avgheartrate;
	}

	public void setAvgheartrate(int avgheartrate) {
		this.avgheartrate = avgheartrate;
	}

	public double getMinaltitude() {
		return minaltitude;
	}

	public void setMinaltitude(double minaltitude) {
		this.minaltitude = minaltitude;
	}

	public double getMaxaltitude() {
		return maxaltitude;
	}

	public void setMaxaltitude(double maxaltitude) {
		this.maxaltitude = maxaltitude;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
