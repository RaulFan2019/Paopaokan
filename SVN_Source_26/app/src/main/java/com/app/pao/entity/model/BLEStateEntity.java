public class BLEStateEntity {
    public  static final int STATE_UNCONNECT = 0;//未连接
    public  static final int STATE_CONNECTING = 1;//连接中
    public  static final int STATE_CONNECTED = 2;//已连接

    public BLEEntity mBleDevice;//Ble device
    public int mConnectState;//ble device connect state

    public BLEStateEntity(BLEEntity mBleDevice, int mConnectState) {
        this.mBleDevice = mBleDevice;
        this.mConnectState = mConnectState;
    }

    public BLEEntity getmBleDevice() {
        return mBleDevice;
    }

    public void setmBleDevice(BLEEntity mBleDevice) {
        this.mBleDevice = mBleDevice;
    }

    public int getmConnectState() {
        return mConnectState;
    }

    public void setmConnectState(int mConnectState) {
        this.mConnectState = mConnectState;
    }
}
