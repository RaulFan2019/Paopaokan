package com.app.pao.entity.event;

import java.io.Serializable;

/**
 * Created by Raul.Fan on 2016/4/13.
 */
public class EventRunningBLE implements Serializable{
    public int heartRate;      //心跳数据

    public EventRunningBLE() {
    }

    public EventRunningBLE(int heartRate) {
        this.heartRate = heartRate;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }
}
