package com.app.pao.entity.model;

import java.io.Serializable;

/**
 * 心率历史(用于展示)
 *
 * @author Raul
 *
 */
public class HistoryHeartrateEntity implements Serializable {

	public long timeofset;// 相对于整个跑步历史的时间偏移
	public int bmp;// 心率

	public HistoryHeartrateEntity(long timeofset, int bmp) {
		super();
		this.timeofset = timeofset;
		this.bmp = bmp;
	}

	public long getTimeofset() {
		return timeofset;
	}

	public void setTimeofset(long timeofset) {
		this.timeofset = timeofset;
	}

	public int getBmp() {
		return bmp;
	}

	public void setBmp(int bmp) {
		this.bmp = bmp;
	}

}
