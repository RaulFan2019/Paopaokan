package com.app.pao.entity.model;

/**
 * 直播位置
 *
 * @author Raul
 */
public class LiveLocation {

    public double latitude;
    public double longitude;
    public float haccuracy;
    public double altitude;
    public float vaccuracy;
    public int speed;// 速度 (秒/米)
    public int timeoffset;// 时间偏移
    public int cameraid;// 摄像头ID
    public int visualcameraid;// 是否可以播放的摄像头ID
    public int camerastatus;// 摄像头播放状态
    public String cameraurl;// 视频播放地址
    public int cameratype;//摄像头类型
    public String cameramsg;//语音播报内容

    public LiveLocation() {

    }

    public LiveLocation(double latitude, double longitude, float haccuracy, double altitude, float vaccuracy, int speed,
                        int timeoffset, int cameraid, int visualcameraid, int camerastatus, String cameraurl, int
                                cameratype, String cameramsg) {
        super();
        this.latitude = latitude;
        this.longitude = longitude;
        this.haccuracy = haccuracy;
        this.altitude = altitude;
        this.vaccuracy = vaccuracy;
        this.speed = speed;
        this.timeoffset = timeoffset;
        this.cameraid = cameraid;
        this.visualcameraid = visualcameraid;
        this.camerastatus = camerastatus;
        this.cameraurl = cameraurl;
        this.cameraid = cameratype;
        this.cameramsg = cameramsg;
    }

    public String getCameraurl() {
        return cameraurl;
    }

    public void setCameraurl(String cameraurl) {
        this.cameraurl = cameraurl;
    }

    public int getCamerastatus() {
        return camerastatus;
    }

    public void setCamerastatus(int camerastatus) {
        this.camerastatus = camerastatus;
    }

    public int getVisualcameraid() {
        return visualcameraid;
    }

    public void setVisualcameraid(int visualcameraid) {
        this.visualcameraid = visualcameraid;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public float getHaccuracy() {
        return haccuracy;
    }

    public void setHaccuracy(float haccuracy) {
        this.haccuracy = haccuracy;
    }

    public double getAltitude() {
        return altitude;
    }

    public void setAltitude(double altitude) {
        this.altitude = altitude;
    }

    public float getVaccuracy() {
        return vaccuracy;
    }

    public void setVaccuracy(float vaccuracy) {
        this.vaccuracy = vaccuracy;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getTimeoffset() {
        return timeoffset;
    }

    public void setTimeoffset(int timeoffset) {
        this.timeoffset = timeoffset;
    }

    public int getCameraid() {
        return cameraid;
    }

    public void setCameraid(int cameraid) {
        this.cameraid = cameraid;
    }

    public int getCameratype() {
        return cameratype;
    }

    public void setCameratype(int cameratype) {
        this.cameratype = cameratype;
    }

    public String getCameramsg() {
        return cameramsg;
    }

    public void setCameramsg(String cameramsg) {
        this.cameramsg = cameramsg;
    }
}
